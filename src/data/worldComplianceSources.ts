export function categorizeSource(source: string): string {
  const lower = source.toLowerCase();
  if (lower.includes("pep")) return "PEP Data";
  if (lower.includes("sanction") || lower.includes("freeze") || lower.includes("terrorist") || lower.includes("proscribed")) return "Sanctions Lists";
  if (lower.includes("police") || lower.includes("interpol") || lower.includes("crime") || lower.includes("investigation") || lower.includes("enforcement") || lower.includes("narcotic") || lower.includes("drug") || lower.includes("wanted") || lower.includes("fugitive")) return "Law Enforcement";
  if (lower.includes("court") || lower.includes("tribunal") || lower.includes("prosecutor") || lower.includes("judiciary") || lower.includes("justice")) return "Courts & Prosecutors";
  if (lower.includes("bank") || lower.includes("financial") || lower.includes("securities") || lower.includes("monetary") || lower.includes("insurance") || lower.includes("stock exchange") || lower.includes("capital market") || lower.includes("supervisory") || lower.includes("finma") || lower.includes("bafin") || lower.includes("fca") || lower.includes("finans")) return "Financial Regulators";
  if (lower.includes("tax") || lower.includes("revenue") || lower.includes("customs") || lower.includes("fiscal")) return "Tax & Revenue";
  if (lower.includes("competition") || lower.includes("antitrust") || lower.includes("antimonopoly") || lower.includes("cartel")) return "Competition Authorities";
  return "Regulatory Bodies";
}

